values ('first_workout', 'First Workout', 'Logged your first set',  '2024-01-01T09:00:00'),
       ('first_workout', 'First Workout', 'Logged your first set',  '2024-06-01T09:00:00'),
       ('ten_workouts',  'Ten Workouts',  'Logged ten sessions',    '2024-03-01T09:00:00');

-- Expect exactly one row: first_workout, copies = 2.
select achievement_id, count(*) as copies
from public.achievements
group by achievement_id
having count(*) > 1;

